/*
 * @file    delay.c
 * @brief   延时功能实现
 */

#include "stm32f10x.h"

//=============================================================================
// 函数名称：Delay_us
// 功能概要：微秒级延时
// 参数说明：us - 延时时间（微秒）
// 返回值：无
//=============================================================================
void Delay_us(uint32_t us)
{
    uint32_t count = us * (SystemCoreClock / 1000000) / 5;
    while (count--)
    {
        __NOP();
    }
}

//=============================================================================
// 函数名称：Delay_ms
// 功能概要：毫秒级延时
// 参数说明：ms - 延时时间（毫秒）
// 返回值：无
//=============================================================================
void Delay_ms(uint32_t ms)
{
    while (ms--)
    {
        Delay_us(1000);
    }
}
